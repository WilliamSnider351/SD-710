class ORLDataset(Dataset):
    def __init__(self, samples: List[Tuple[Path, int]], transform=None):
        self.samples = samples
        self.transform = transform

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, idx):
        img_path, label = self.samples[idx]
        img = Image.open(img_path).convert("L")
        if self.transform:
            img = self.transform(img)
        return img, label


def build_splits(orl_dir: Path):
    train, val, test = [], [], []

    subjects = sorted([d for d in orl_dir.iterdir() if d.is_dir() and d.name.startswith("s")])
    assert len(subjects) == 40

    for label, subj_dir in enumerate(subjects):
        images = sorted(list(subj_dir.glob("*.pgm")))
        assert len(images) == 10

        train_imgs = images[:6]
        val_imgs   = images[6:8]
        test_imgs  = images[8:10]

        train.extend([(p, label) for p in train_imgs])
        val.extend([(p, label) for p in val_imgs])
        test.extend([(p, label) for p in test_imgs])

    return train, val, test


