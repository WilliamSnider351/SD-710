class ORLCNN(nn.Module):
    def __init__(self, embedding_dim=128, num_classes=40):
        super().__init__()

        self.features = nn.Sequential(
            nn.Conv2d(1, 32, 5, padding=2),
            nn.BatchNorm2d(32),
            nn.ReLU(),
            nn.MaxPool2d(2),

            nn.Conv2d(32, 64, 3, padding=1),
            nn.BatchNorm2d(64),
            nn.ReLU(),
            nn.MaxPool2d(2),

            nn.Conv2d(64, 128, 3, padding=1),
            nn.BatchNorm2d(128),
            nn.ReLU(),
            nn.MaxPool2d(2),

            nn.Conv2d(128, 128, 3, padding=1),
            nn.BatchNorm2d(128),
            nn.ReLU(),
            nn.AdaptiveAvgPool2d((1, 1)),
        )

        # Embedding head
        self.embedding = nn.Linear(128, embedding_dim)

        # Classification head
        self.classifier = nn.Linear(embedding_dim, num_classes)

    def forward(self, x):
        x = self.features(x)
        x = torch.flatten(x, 1)
        emb = F.relu(self.embedding(x))
        logits = self.classifier(emb)
        return logits

    def extract_embeddings(self, x):
        """Used for verification accuracy."""
        x = self.features(x)
        x = torch.flatten(x, 1)
        emb = F.relu(self.embedding(x))
        return emb


