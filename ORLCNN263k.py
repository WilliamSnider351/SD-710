def accuracy_from_logits(logits, labels):
    """
    Computes simple classification accuracy.

    """
    preds = torch.argmax(logits, dim=1)
    correct = (preds == labels).sum().item()
    total = labels.size(0)
    return correct, total


def evaluate(model, loader, criterion):
    """
    Computes:
    - average loss
    - classification accuracy
    on the given loader (val or test)

    """
    model.eval()
    total_loss = 0.0
    total_correct = 0
    total_samples = 0

    with torch.no_grad():
        for imgs, labels in loader:
            imgs = imgs.to(device)
            labels = labels.to(device)

            logits = model(imgs)
            loss = criterion(logits, labels)

            total_loss += loss.item() * imgs.size(0)

            correct, total = accuracy_from_logits(logits, labels)
            total_correct += correct
            total_samples += total

    # SAFETY CHECK — prevents returning None
    if total_samples == 0:
        print("WARNING: evaluate() received an empty loader.")
        return 0.0, 0.0

    avg_loss = total_loss / total_samples
    avg_acc = total_correct / total_samples

    return avg_loss, avg_acc

# CELL 6.5 — Verification Accuracy (pairwise cosine similarity)

def compute_verification_accuracy(model, loader):
    """
    Computes face-verification accuracy using cosine similarity.
    """
    model.eval()
    all_embs = []
    all_labels = []

    # Extract embeddings for the entire loader
    with torch.no_grad():
        for imgs, labels in loader:
            imgs = imgs.to(device)
            emb = model.extract_embeddings(imgs)
            all_embs.append(emb.cpu())
            all_labels.append(labels)

    all_embs = torch.cat(all_embs)
    all_labels = torch.cat(all_labels)

    pos_sims = []
    neg_sims = []

    # Build all pairwise comparisons
    for i, j in itertools.combinations(range(len(all_labels)), 2):
        sim = F.cosine_similarity(
            all_embs[i].unsqueeze(0),
            all_embs[j].unsqueeze(0)
        ).item()

        if all_labels[i] == all_labels[j]:
            pos_sims.append(sim)
        else:
            neg_sims.append(sim)

    pos_sims = np.array(pos_sims)
    neg_sims = np.array(neg_sims)

    # Sweep thresholds from -1 to +1
    thresholds = np.linspace(-1, 1, 200)
    best_acc = 0.0

    for t in thresholds:
        tp = np.sum(pos_sims >= t)
        fn = np.sum(pos_sims < t)
        tn = np.sum(neg_sims < t)
        fp = np.sum(neg_sims >= t)

        acc = (tp + tn) / (tp + tn + fp + fn)
        best_acc = max(best_acc, acc)

    return best_acc

