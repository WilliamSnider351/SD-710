from tqdm import tqdm

num_epochs = 20
learning_rate = 1e-3
weight_decay = 1e-4

criterion = nn.CrossEntropyLoss()
optimizer = torch.optim.Adam(model.parameters(), lr=learning_rate, weight_decay=weight_decay)

best_verif = 0.0
best_state = None

for epoch in range(1, num_epochs + 1):
    model.train()
    total_loss = 0.0
    total_correct = 0
    total_samples = 0

    # -------------------------
    # Training loop
    # -------------------------
    for imgs, labels in tqdm(train_loader, desc=f"Epoch {epoch}/{num_epochs}", leave=False):
        imgs = imgs.to(device)
        labels = labels.to(device)

        optimizer.zero_grad()
        logits = model(imgs)
        loss = criterion(logits, labels)
        loss.backward()
        optimizer.step()

        total_loss += loss.item() * imgs.size(0)
        correct, total = accuracy_from_logits(logits, labels)
        total_correct += correct
        total_samples += total

    train_loss = total_loss / total_samples
    train_acc = total_correct / total_samples

    # -------------------------
    # Validation (classification)
    # -------------------------
    val_loss, val_acc = evaluate(model, val_loader, criterion)

    # -------------------------
    # Validation (verification accuracy)
    # -------------------------
    val_verif = compute_verification_accuracy(model, val_loader)

    # Track best model by verification accuracy
    if val_verif > best_verif:
        best_verif = val_verif
        best_state = {k: v.cpu() for k, v in model.state_dict().items()}

    # -------------------------
    # Epoch summary
    # -------------------------
    print(
        f"Epoch {epoch:02d} | "
        f"Train Loss: {train_loss:.4f} | Train Acc: {train_acc*100:.2f}% | "
        f"Val Loss: {val_loss:.4f} | Val Acc: {val_acc*100:.2f}% | "
        f"Val Verification Acc: {val_verif*100:.2f}%"
    )

# -------------------------
# Load best model
# -------------------------
if best_state is not None:
    model.load_state_dict(best_state)
